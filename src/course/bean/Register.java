package course.bean;

public class Register
{
  public int id;
  public String name;
  public String email;
  public String password;
  public String phone;
  public String username;
  public String address;
  public String department;
  public int active;
  public String studentRegistrationDate;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getPhone() {
	return phone;
}
public void setPhone(String phone) {
	this.phone = phone;
}
public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public String getDepartment() {
	return department;
}
public void setDepartment(String department) {
	this.department = department;
}
public int getActive() {
	return active;
}
public void setActive(int active) {
	this.active = active;
}
public String getStudentRegistrationDate() {
	return studentRegistrationDate;
}
public void setStudentRegistrationDate(String studentRegistrationDate) {
	this.studentRegistrationDate = studentRegistrationDate;
}
  
}
